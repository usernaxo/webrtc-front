import { useEffect, useRef } from "react";

const VideoPlayer = ({ stream, muted = false, className }) => {
  
  const videoRef = useRef(null);

  useEffect(() => {
    if (videoRef.current && stream) {
      videoRef.current.srcObject = stream;
    }
  }, [stream]);

  return (
    <video
      ref={videoRef}
      autoPlay
      playsInline
      muted={muted}
      className={className}
    />
  );

};

export default VideoPlayer;
